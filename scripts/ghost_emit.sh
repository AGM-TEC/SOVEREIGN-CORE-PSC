#!/bin/bash
# 🛡️ SOVEREIGN CORE - GHOST EMISSION (DECEPTION)
echo "🎭 Initialisation des leurres radioélectroniques..."

# Génère des pics de signal aléatoires imitant des terminaux Motorola
# Cela sature les systèmes d'alerte ennemis.
while true; do
  # Simulation de bursts radio sur des fréquences aléatoires
  FREQ=$(( ( RANDOM % 100 )  + 2400 ))
  echo "📡 [GHOST] Émission d'un leurre sur ${FREQ}MHz"
  sleep 2
done
